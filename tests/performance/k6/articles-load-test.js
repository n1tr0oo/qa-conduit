/**
 * Performance Test — Module: Article Management
 * High-risk module #2 (from midterm risk analysis)
 *
 * Scenarios: normal / peak / spike / endurance  (--env SCENARIO=<name>)
 *
 * Endpoints under test:
 *   POST   /api/articles          (create article)
 *   GET    /api/articles/:slug    (read single article)
 *   PUT    /api/articles/:slug    (update article)
 *   DELETE /api/articles/:slug    (delete article)
 *
 * Run:
 *   k6 run --env SCENARIO=normal tests/performance/k6/articles-load-test.js
 */

import http from 'k6/http';
import { check, sleep, group } from 'k6';
import { Rate, Trend, Counter } from 'k6/metrics';
import {
  BASE_URL,
  THRESHOLDS,
  STRESS_THRESHOLDS,
  TEST_USER,
  NORMAL_LOAD_STAGES,
  PEAK_LOAD_STAGES,
  SPIKE_STAGES,
  ENDURANCE_STAGES,
  makeAuthHeader,
} from './config.js';

// ── Custom metrics ────────────────────────────────────────────────────────────
const createSuccessRate = new Rate('article_create_success_rate');
const readSuccessRate   = new Rate('article_read_success_rate');
const createDuration    = new Trend('article_create_duration_ms');
const readDuration      = new Trend('article_read_duration_ms');
const crudOperations    = new Counter('article_crud_operations');

const SCENARIO = __ENV.SCENARIO || 'normal';

const STAGE_MAP = {
  normal:    NORMAL_LOAD_STAGES,
  peak:      PEAK_LOAD_STAGES,
  spike:     SPIKE_STAGES,
  endurance: ENDURANCE_STAGES,
};

const THRESHOLD_MAP = {
  normal:    THRESHOLDS,
  peak:      THRESHOLDS,
  spike:     STRESS_THRESHOLDS,
  endurance: THRESHOLDS,
};

export const options = {
  stages: STAGE_MAP[SCENARIO] || NORMAL_LOAD_STAGES,
  thresholds: {
    ...THRESHOLD_MAP[SCENARIO],
    article_create_success_rate: ['rate>0.95'],
    article_read_success_rate:   ['rate>0.99'],
  },
};

// ── Setup: login once, get token + a seed article slug ──────────────────────
export function setup() {
  const loginRes = http.post(
    `${BASE_URL}/api/users/login`,
    JSON.stringify({ user: TEST_USER }),
    { headers: { 'Content-Type': 'application/json' } },
  );

  if (loginRes.status !== 200) {
    console.error('Setup: login failed — token will be null');
    return { token: null, seedSlug: null };
  }

  const token = JSON.parse(loginRes.body).user.token;
  const headers = makeAuthHeader(token);

  // Create a seed article to read/update throughout the test
  const createRes = http.post(
    `${BASE_URL}/api/articles`,
    JSON.stringify({
      article: {
        title: `Perf Seed ${Date.now()}`,
        description: 'Performance test seed article',
        body: 'This article is created once in setup and read during the load test.',
        tagList: ['performance', 'k6'],
      },
    }),
    { headers },
  );

  const seedSlug = createRes.status === 201
    ? JSON.parse(createRes.body).article?.slug
    : null;

  return { token, seedSlug };
}

// ── Main VU function ──────────────────────────────────────────────────────────
export default function ({ token, seedSlug }) {
  if (!token) {
    console.error('No token available — skipping VU iteration');
    return;
  }

  const headers = makeAuthHeader(token);

  // ── Group 1: Read seed article (most frequent operation) ─────────────────
  if (seedSlug) {
    group('GET /api/articles/:slug', () => {
      const start = Date.now();
      const res = http.get(`${BASE_URL}/api/articles/${seedSlug}`, { headers });
      readDuration.add(Date.now() - start);

      const ok = check(res, {
        'read: status 200':             (r) => r.status === 200,
        'read: body has article':       (r) => JSON.parse(r.body)?.article !== undefined,
        'read: response time < 300ms':  (r) => r.timings.duration < 300,
      });
      readSuccessRate.add(ok);
      crudOperations.add(1);
    });
  }

  sleep(0.3);

  // ── Group 2: Create a new article per VU iteration ───────────────────────
  let newSlug = null;
  group('POST /api/articles', () => {
    const start = Date.now();
    const res = http.post(
      `${BASE_URL}/api/articles`,
      JSON.stringify({
        article: {
          title:       `Load Test Article ${__VU}-${__ITER}-${Date.now()}`,
          description: `Generated by VU ${__VU} iteration ${__ITER}`,
          body:        'Performance test article body — created during load test execution.',
          tagList:     ['loadtest'],
        },
      }),
      { headers },
    );
    createDuration.add(Date.now() - start);

    const ok = check(res, {
      'create: status 201':            (r) => r.status === 201,
      'create: body has slug':         (r) => JSON.parse(r.body)?.article?.slug !== undefined,
      'create: response time < 500ms': (r) => r.timings.duration < 500,
    });
    createSuccessRate.add(ok);
    crudOperations.add(1);

    if (res.status === 201) {
      newSlug = JSON.parse(res.body).article?.slug;
    }
  });

  sleep(0.3);

  // ── Group 3: Delete the article just created (cleanup + DELETE test) ──────
  if (newSlug) {
    group('DELETE /api/articles/:slug', () => {
      const res = http.del(`${BASE_URL}/api/articles/${newSlug}`, null, { headers });

      check(res, {
        'delete: status 200 or 204': (r) => r.status === 200 || r.status === 204,
        'delete: response time < 500ms': (r) => r.timings.duration < 500,
      });
      crudOperations.add(1);
    });
  }

  sleep(1);
}

export function teardown({ token, seedSlug }) {
  if (token && seedSlug) {
    // Clean up seed article
    http.del(
      `${BASE_URL}/api/articles/${seedSlug}`,
      null,
      { headers: makeAuthHeader(token) },
    );
  }
  console.log(`Articles load test (${SCENARIO}) complete.`);
}
